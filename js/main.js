// DOM Elements
const mobileMenuBtn = document.querySelector('.mobile-menu-btn');
const navLinks = document.querySelector('.nav-links');
const body = document.body;
const langBtn = document.getElementById('langBtn');
const htmlElement = document.documentElement;
const tabButtons = document.querySelectorAll('.tab-btn');
const tabContents = document.querySelectorAll('.tab-content');
const sections = document.querySelectorAll('.section');
const applicationForm = document.getElementById('applicationForm');
const contactForm = document.getElementById('contactForm');
const stayUpdated = document.getElementById('newsletter-form');

// Variables
let resizeTimer;
let currentLang = 'en';
const observerOptions = {
    threshold: 0.1
};
const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.style.opacity = '1';
            entry.target.style.transform = 'translateY(0)';
        }
    });
}, observerOptions);

// Mobile Menu Functions
function toggleMobileMenu() {
    navLinks.classList.toggle('active');
    body.style.overflow = navLinks.classList.contains('active') ? 'hidden' : '';
}

// Initialize Mobile Menu
if (mobileMenuBtn) {
    mobileMenuBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        toggleMobileMenu();
    });

    // Close mobile menu when clicking outside
    document.addEventListener('click', (e) => {
        if (navLinks.classList.contains('active') && 
            !e.target.closest('.nav-links') && 
            !e.target.closest('.mobile-menu-btn')) {
            toggleMobileMenu();
        }
    });

    // Close mobile menu when clicking a link
    document.querySelectorAll('.nav-links a').forEach(link => {
        link.addEventListener('click', () => {
            if (window.innerWidth <= 768) {
                toggleMobileMenu();
            }
        });
    });
}

// Smooth Scrolling
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }
    });
});

// Form Handling
if (applicationForm) {
    applicationForm.addEventListener('submit', function(e) {
        e.preventDefault();
        alert('Thank you for your application! We will review it and get back to you soon.');
        this.reset();
    });
}

if (contactForm) {
    contactForm.addEventListener('submit', function(e) {
        e.preventDefault();
        alert('Thank you for your message! We will get back to you soon.');
        this.reset();
    });
}

if (stayUpdated) {
    stayUpdated.addEventListener('submit', function(e) {
        e.preventDefault();
        alert('Thank you for your interest! We will keep you updated.');
        this.reset();
    });
}

// Navbar Scroll Effect
window.addEventListener('scroll', function() {
    const navbar = document.querySelector('.navbar');
    if (window.scrollY > 50) {
        navbar.style.backgroundColor = 'rgba(255, 255, 255, 0.95)';
        navbar.style.boxShadow = '0 2px 5px rgba(0,0,0,0.1)';
    } else {
        navbar.style.backgroundColor = 'var(--white)';
        navbar.style.boxShadow = 'none';
    }
});

// Window Resize Handler
window.addEventListener('resize', () => {
    clearTimeout(resizeTimer);
    resizeTimer = setTimeout(() => {
        if (window.innerWidth > 768) {
            navLinks.classList.remove('active');
            body.style.overflow = '';
        }
    }, 250);
});

// Section Animation
sections.forEach(section => {
    section.style.opacity = '0';
    section.style.transform = 'translateY(20px)';
    section.style.transition = 'opacity 0.5s ease, transform 0.5s ease';
    observer.observe(section);
});

// Curriculum Tabs
if (tabButtons.length > 0) {
    tabButtons.forEach(button => {
        button.addEventListener('click', () => {
            // Remove active class from all buttons and contents
            tabButtons.forEach(btn => btn.classList.remove('active'));
            tabContents.forEach(content => content.classList.remove('active'));
            
            // Add active class to clicked button
            button.classList.add('active');
            
            // Show corresponding content
            const tabId = button.getAttribute('data-tab');
            const content = document.getElementById(tabId);
            if (content) {
                content.classList.add('active');
            }
        });
    });
}

// Language Switcher
if (langBtn) {
    // Toggle language on button click
    langBtn.addEventListener('click', () => {
        currentLang = currentLang === 'en' ? 'fr' : 'en';
        updateLanguage(currentLang);
    });
}

// Update language
function updateLanguage(lang) {
    // Update button text
    const langBtnText = document.querySelector('.lang-btn span');
    if (langBtnText) {
        langBtnText.textContent = lang === 'en' ? 'FR' : 'EN';
    }

    // Update translations
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.dataset.translate;
        if (translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Update form placeholders and options
    const forms = document.querySelectorAll('form');
    forms.forEach(form => {
        const inputs = form.querySelectorAll('input, textarea, select');
        inputs.forEach(input => {
            if (input.placeholder) {
                const key = input.placeholder.toLowerCase().replace(/\s+/g, '');
                if (translations[lang][key]) {
                    input.placeholder = translations[lang][key];
                }
            }
            if (input.tagName === 'SELECT') {
                const options = input.querySelectorAll('option');
                options.forEach(option => {
                    if (option.value) {
                        const key = option.value.toLowerCase().replace(/\s+/g, '');
                        if (translations[lang][key]) {
                            option.textContent = translations[lang][key];
                        }
                    }
                });
            }
        });
    });

    // Update course lists
    const courseLists = document.querySelectorAll('.semester ul li');
    courseLists.forEach((item, index) => {
        const key = `course${index + 1}`;
        if (translations[lang][key]) {
            item.textContent = translations[lang][key];
        }
    });

    // Update career descriptions
    const careerItems = document.querySelectorAll('.career-card ul li');
    careerItems.forEach((item, index) => {
        const key = `career${Math.floor(index/3) + 1}Desc${(index % 3) + 1}`;
        if (translations[lang][key]) {
            item.textContent = translations[lang][key];
        }
    });
}

// Initialize
updateLanguage('en'); 